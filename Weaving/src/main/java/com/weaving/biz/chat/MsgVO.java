package com.weaving.biz.chat;

public class MsgVO {

	private String cmd;
	private String tmsg;
	private String id;

	public String getCmd() {
		return cmd;
	}

	public void setCmd(String cmd) {
		this.cmd = cmd;
	}

	public String getTmsg() {
		return tmsg;
	}

	public void setTmsg(String tmsg) {
		this.tmsg = tmsg;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
